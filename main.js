for (let i = 0; i < level_1.length; i++) {
  grids[i] = []
  for (let j = 0; j < level_1[i].length; j++) {
    const div = document.createElement("div");
    map.appendChild(div);
    grids[i].push(div);
    switch (level_1[i][j]) {
      case 0:
        grids[i][j].classList.add("pac-dot");
        continue;
      case 1:
        grids[i][j].classList.add("wall");
        continue;
      case 2:
        grids[i][j].classList.add("ghost-lair");
        continue;
      case 3:
        grids[i][j].classList.add("power-pellet");
        continue;
      case 4:
        grids[i][j].classList.add("empy");
        continue;
      case 5:
        grids[i][j].classList.add("teleport");
      default:
        continue;
    }
  }
}

let pacmanCurrentIndex = 490;
grids[pacmanCurrentIndex].classList.add("pac-man");

const playerMovement = (event) => {
  switch (event.key.toLowerCase()) {
    case "arrowup":
    case "w":
      pacmanCurrentIndex += 1;
      grids[pacmanCurrentIndex].classList.remove("pac-man");
      grids[pacmanCurrentIndex].classList.add("pac-man");
      break;
    case "arrowdown":
    case "s":
      pacmanCurrentIndex -= 1;
      grids[pacmanCurrentIndex].classList.remove("pac-man");
      grids[pacmanCurrentIndex].classList.add("pac-man");
      break;
    case "arrowright":
    case "d":
      pacmanCurrentIndex += 1;
      grids[pacmanCurrentIndex].classList.remove("pac-man");
      grids[pacmanCurrentIndex].classList.add("pac-man");
      break;
    case "arrowleft":
    case "a":
      pacmanCurrentIndex -= 1;
      grids[pacmanCurrentIndex].classList.remove("pac-man");
      grids[pacmanCurrentIndex].classList.add("pac-man");
      break;
    default:
      break;
  }
};

document.addEventListener("keyup", playerMovement);
